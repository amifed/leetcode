// @lc code=start

int climbStairs(int n)
{
    int i;
    long f0 = 1, f1 = 2, fn;
    for (i = 2; i <= n; i++)
    {
        fn = f0 + f1;
        f0 = f1;
        f1 = fn;
    }
    return f0;
}

